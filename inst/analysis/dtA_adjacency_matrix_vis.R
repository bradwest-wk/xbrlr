# =============================================================================
#                         Taxonomy Adjacency Matrix
# -----------------------------------------------------------------------------
# The current U.S. GAAP taxonomy is too large to effectively be plotted in an
# a network graph. The network graph more or less fails to preserve the directed
# parent-child nature of the taxonomy and ends up looking like a hairball. An
# alternative would be to plot the data as a zoomable adjacency matrix, with
# nodes color coordinated by statement. We can plot a directed adjacency
# matrix as well, which is required for this type of visualization.
# =============================================================================